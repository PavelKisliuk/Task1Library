package com.epam.training.converter.inf;

import com.epam.training.converter.Converter;

public class Byte implements Converter {
	//-----------------------------------------------------------------------------
	private static final double KILOBYTE = 1024.0;
	private static final double MEGABYTE = 1_048_576.0;
	private static final double GIGABYTE = 1_073_741_824.0;
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
	public static double toKilo(double b)
	{
		return (b / Byte.KILOBYTE);
	}
	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
	public static double toMega(double b)
	{
		return (b / Byte.MEGABYTE);
	}
	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
	public static double toGiga(double b)
	{
		return (b / Byte.GIGABYTE);
	}
	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
}
