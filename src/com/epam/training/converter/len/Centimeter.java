package com.epam.training.converter.len;

import com.epam.training.converter.Converter;

public class Centimeter implements Converter {
	//-----------------------------------------------------------------------------
	private static final double METER = 1000.0;
	private static final double KILOMETER = 100_000.0;
	//-----------------------------------------------------------------------------

	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
	public static double toKilo(double c)
	{
		return (c / Centimeter.METER);
	}
	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
	public static double toMega(double c)
	{
		return (c / Centimeter.KILOMETER);
	}
	//-----------------------------------------------------------------------------
	//-----------------------------------------------------------------------------
}
